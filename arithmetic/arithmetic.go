package main

import "fmt"

func main() {
	//	浮点数计算
	//	加法
	fmt.Println(1.0 + 2.0)
	//	减法
	fmt.Println(1.0 - 2.0)
	//	乘法
	fmt.Println(1.0 * 2.0)
	//	除法
	fmt.Println(1.0 / 2.0)

	//	整数计算
	//	加法
	fmt.Println(1 + 2)
	//	减法
	fmt.Println(1 - 2)
	//	乘法
	fmt.Println(1 * 2)
	//	除法
	fmt.Println(1 / 2)
	//	求余
	fmt.Println(1 % 2)

}
